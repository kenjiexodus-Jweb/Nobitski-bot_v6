    try { await api.setMessageReaction("üé®", messageID, () => {}, true); } catch(e){}
    api.sendMessage(`üé® ùóöùó≤ùóªùó≤ùóøùóÆùòÅùó∂ùóªùó¥ ùóúùó∫ùóÆùó¥ùó≤ ùó≥ùóºùóø: "${prompt}"... please wait.`, threadID, messageID);

